    ,REGION        VARCHAR
    ,PRODUCT_ID    VARCHAR
    ,CATEGORY      VARCHAR
    ,SUB_CATEGORY  VARCHAR
    ,PRODUCT_NAME  VARCHAR
    ,SALES         VARCHAR
    ,QUANTITY      VARCHAR
    ,DISCOUNT      VARCHAR
    ,PROFIT        VARCHAR
);


-- -----------------------------------------------------------------------------
-- STAGE LAYER — views that trim whitespace and cast types
-- -----------------------------------------------------------------------------

CREATE OR REPLACE VIEW RETAIL_DB.STAGE.VW_D_COUNTRY (
    COUNTRY_NAME
) AS
