-- -

CREATE OR REPLACE VIEW RETAIL_DB.STAGE.VW_F_SALES (
     ROW_ID
    ,ORDER_ID
    ,ORDER_DATE
    ,SHIP_DATE
    ,CUSTOMER_ID
    ,PRODUCT_ID
    ,COUNTRY
    ,REGION
    ,STATE
    ,CITY
    ,POSTAL_CODE
    ,SHIP_MODE
    ,QUANTITY
    ,SALES
    ,DISCOUNT
    ,DISCOUNT_AMOUNT
    ,REVENUE
    ,PROFIT
) AS
SELECT
     TRIM(ROW_ID)
    ,TRIM(ORDER_ID)
    ,TO_DATE(ORDER_DATE, 'MM/DD/YYYY')
    ,TO_DATE(SHIP_DATE, 'MM/DD/YYYY')
    ,TRIM(CUSTOMER_ID)
    ,TRIM(PRODUCT_ID)
    ,TRIM(COUNTRY)
    ,TRIM(REGION)
    ,TRIM(STATE)
    ,TRIM(CITY)
    ,TRIM(POSTAL_CODE)
    ,TRIM(SHIP_MODE)
    ,TRIM(QUANTITY)::NUMBER
    ,TRIM(SALES)::FLOAT
    ,TRIM(DISCOUNT)::FLOAT
    ,(TRIM(SALES)::FLOAT * TRIM(DISCOUNT)::FLOAT)           AS DISCOUNT_AMOUNT
    ,(TRIM(SALES)::FLOAT * (1 - TRIM(DISCOUNT)::FLOAT))     AS REVENUE
    ,TRIM(PROFIT)::FLOAT
FROM RETAIL_DB.LANDING.RAW_SALES
WHERE ROW_ID IS NOT NULL;


